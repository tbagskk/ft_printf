#include "ft_printf.h"

int	ft_u(unsigned long a)
{
	unsigned long	b;
	int				i;
	char			tab[12];

	i = 0;
	while (a)
	{
		tab[i] = ('0' + a % 10);
		a = a / 10;
		i++;
	}
	while (i)
	{
		i--;
		ft_putchar(tab[i]);
	}
	return (1);
}
